package study1.method;

public class MethodEx1 {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;

        int sum = a + b + c;
        double average = sum / 3.0;
        System.out.println("평균값: " + average);

        int x = 15;
        int y = 20;
        int z = 30;

        sum = x + y + z;
        average = sum / 3.0;
        System.out.println("평균값: " + average);
    }
}
